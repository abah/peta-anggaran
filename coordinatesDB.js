function getCoordinates(provinsi, kabupaten) {
    if (!provinsi || !kabupaten) {
        console.warn('Provinsi atau kabupaten kosong:', { provinsi, kabupaten });
        return { lat: -2.5489, lng: 118.0149 }; // Default ke pusat Indonesia
    }

    // Normalisasi input
    provinsi = provinsi.trim().toLowerCase();
    kabupaten = kabupaten.trim().toLowerCase();

    // Coba cari match langsung
    for (const key in coordinatesDB) {
        const [dbProvinsi, dbKabupaten] = key.split('|');
        if (dbProvinsi.toLowerCase().includes(provinsi) && 
            dbKabupaten.toLowerCase().includes(kabupaten)) {
            console.log('Match langsung ditemukan:', key);
            return {
                lat: coordinatesDB[key][0],
                lng: coordinatesDB[key][1]
            };
        }
    }

    // Jika tidak ada match langsung, coba cari kabupaten saja
    for (const key in coordinatesDB) {
        const [, dbKabupaten] = key.split('|');
        if (dbKabupaten.toLowerCase().includes(kabupaten)) {
            console.log('Match kabupaten ditemukan:', key);
            return {
                lat: coordinatesDB[key][0],
                lng: coordinatesDB[key][1]
            };
        }
    }

    // Jika masih tidak ketemu, coba cari ibukota provinsi
    for (const key in coordinatesDB) {
        const [dbProvinsi] = key.split('|');
        if (dbProvinsi.toLowerCase().includes(provinsi)) {
            console.log('Match provinsi ditemukan:', key);
            return {
                lat: coordinatesDB[key][0],
                lng: coordinatesDB[key][1]
            };
        }
    }

    // Jika tidak ada yang cocok, kembalikan koordinat default
    console.warn('Tidak ditemukan koordinat untuk:', { provinsi, kabupaten });
    return { lat: -2.5489, lng: 118.0149 }; // Default ke pusat Indonesia
} 